# equality comparisons

# to compare two values are identical
# use == or !=

# equality
print(42 == 42)     # True
print(42 == 43)     # False
print('foo' == 'foo')   # True 
print('FOO' == 'foo')   # False

# inequality
print(42 != 42)     # False
print(42 != 43)     # True
print('foo' != 'foo')   # False
print('FOO' != 'foo')   # True
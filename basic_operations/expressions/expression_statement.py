# expression & statement

# expressions
# combines values, variables, operators, and calls to function to produce a new object
# expressions must be evaluated to determine expression's value

# e.g
# literals: 'Karl', 3.141592, True, None
# var references: foo or name which are predefined
# arithmetic ops: x + y, a * b - 5
# comparison ops: 'x' == 'x', 'x' < 'y'
# string ops: 'x' + 'y', 'x' * 32
# function calls: print('Hello') or 
# len('Python')
# any valid combination of the above that evaluates to single object


# statement
# instruction that tells python to perform an action
# statments do not return values

# e.g.
# assigment - x = 5; assigns value to variable
# control flow: if, else, while, for - determine flow of program
# fn & class def: def or class
# return statement: return statements tell function to exit & output a value; return doesn't return value; it tells function to return value
# import statements: import date, import math
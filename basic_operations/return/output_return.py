# output vs return values

# when the print function is invoked, python is told to output something to display

# e.g
print('Hello')              # 'Hello'

# in this instance, python is giving an output - 'Hello'

#  returning a value
list(range(5))      # [0, 1, 2, 3, 4]

# in the REPL, you may see this output but it's python's way of saying the program returned a value

# when you call a function which returns a value, the value may not printed like in the REPL
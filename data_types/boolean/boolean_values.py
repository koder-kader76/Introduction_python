# boolean values
#
# only 2 - True / False

# booleans can act as numeric values
3 + True # 4
True == 1 # True

# use boolean for on/off app
# e.g
toggle_on = True
session_active = False

# booleans are almost always used with numeric comparisons
42 == 42 # True
39 > 42  # False
42 == 42.0 # True
3.14e+20 == 3.14e+20 # True
3.14e+20 >= 3.14e+20 # True
3.14e+20 <= 3.14e-20 # False
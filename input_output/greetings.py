# example 1 - greeting user by name by hardcoding

name = "Jane"
print(f'Good morning, {name}!')

# example: greet user by name using input()
print("What's your name?")
name = input()

print(f"Good Morning, {name}!")

# What's your name?
# John
# Good Morning, John!

# example of using input function to display prompt
name = input("What's your name? ")

print(f"Good Morning, {name}!")

# What's your name? John
# Good Morning, John!

# example of using input() with new line
name = input("What's your name?\n")

print(f"Good Morning, {name}!")

# What's your name?
# John
# Good Morning, John!
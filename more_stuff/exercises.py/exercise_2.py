# 2. Use the sqrt function from the math library to write some code that outputs the square root of 37. Try to write the code in three different ways.

# 1st way
import math

print(math.sqrt(37))
# 6.082762530298219

# 2nd way
from math import sqrt

print(sqrt(37))
# 6.082762530298219

# 3rd way
import math as m

print(m.sqrt(37))
# 6.082762530298219
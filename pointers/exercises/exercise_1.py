# 1. In your own words, explain the difference between these two expressions.

# my_object1 == my_object2
# my_object1 is my_object2

# both expressions lead to a question of equality & identity

# my_object1 == my_object2
# this is an equality expression - Python determines if both these variables have the same values
# return True if both objects have the same values, False if not
# even though both values are the same doesn't mean that both variables are pointing to the same object


# my_object1 is my_object2
# this expression is an identity expression
# the is operator determines if both variables are referencing the same object
# returns True if yes, False if not
# Naming conventions

# variables
# - use snake_case
# - names can contain lowercase letters, digits and underscores
# - names should begin with letter
# - separate multiple words with underscores
# - names beginning with 1 or 2 underscores have special meaning - DO NOT USE
# - names may only use letters & digits from std ASCII char set

# e.g idiomatic names
# foo
# answer_to_ultimate_question
# eighty_seven
# index_2
# index2

# Constant Names
# use SCREAMING_SNAKE_CASE
# names may contain uppercase letters, digits & underscores
# names should begin with a letter
# separate names with single underscore
# names starting with 1/2 underscores - have special meaning - DO NO USE
# only use letters & digits from ASCII char set

# e.g
# FOO
# ANSWER_TO_ULTIMATE_QUESTION
# EIGHTY_SEVEN
# INDEX_2
# INDEX


# Class Names
# use PascalCase (aka CamelCase)
# has both uppercase & lowercase letters & digits
# names begin with capitalised letter
# multiple words must be capitalized

# e.g
# FOO
# UltimateQuestion
# FourLeggedPets
# PythonVersion2Rules

# Non-Idiomatic Names 
# use letters, digits & underscores - ext ASCII allowed
# noi punctuation, special char & whitespace
# do not start with digit
# do not use reserved words - if, def, while, return, pass

# non-idiomatic names e.g
# fourWayIntersection
# Sch√∂n

# illgal names
# pass  -   reserved word
# 3xy   -   starts with digit
# ultimate-question -   Hyphen
# one two three     -   whitespace
# is_lowercase?     -   punctuation
# is_lowercase      -   special char

# e.g of illegal name
